-- Store the tenant ID for use in subsequent inserts
DO $$
DECLARE
    v_tenant_id UUID;
    v_user_id UUID;
    v_hashed_password TEXT;
BEGIN
    -- Get or create the system tenant
    SELECT id INTO v_tenant_id 
    FROM tenants 
    WHERE name = 'WeedGo System'
    LIMIT 1;
    
    IF v_tenant_id IS NULL THEN
        INSERT INTO tenants (
            id,
            name,
            status,
            subscription_tier,
            created_at,
            updated_at
        ) VALUES (
            gen_random_uuid(),
            'WeedGo System',
            'active',
            'enterprise',
            NOW(),
            NOW()
        ) RETURNING id INTO v_tenant_id;
    END IF;

    -- Generate a UUID for the user
    v_user_id := gen_random_uuid();
    
    -- Hash the password using bcrypt
    -- Note: This is the bcrypt hash for 'your_password_here'
    -- In production, this should be generated by the application
    v_hashed_password := '$2b$12$qGed8TgtYgNsF2PH6V/AqeDv8V/jXMTmTyRbRhm/1UdxqrBh1737u';

    -- Create the super admin user
    INSERT INTO users (
        id,
        email,
        password_hash,
        first_name,
        last_name,
        role,
        is_active,
        email_verified,
        created_at,
        updated_at
    ) VALUES (
        v_user_id,
        'admin@weedgo.ca',
        v_hashed_password,
        'System',
        'Administrator',
        'super_admin',
        true,
        true,
        NOW(),
        NOW()
    ) ON CONFLICT (email) DO UPDATE SET
        password_hash = v_hashed_password,
        role = 'super_admin',
        is_active = true,
        email_verified = true,
        updated_at = NOW();

    -- Link the super admin to the system tenant
    INSERT INTO tenant_users (
        id,
        tenant_id,
        user_id,
        role,
        is_active,
        created_at,
        updated_at
    ) VALUES (
        gen_random_uuid(),
        v_tenant_id,
        v_user_id,
        'admin',
        true,
        NOW(),
        NOW()
    ) ON CONFLICT DO NOTHING;

    RAISE NOTICE 'Super admin user created successfully';
    RAISE NOTICE 'Email: admin@weedgo.ca';
    RAISE NOTICE 'Password: your_password_here';
    RAISE NOTICE 'Tenant ID: %', v_tenant_id;
    RAISE NOTICE 'User ID: %', v_user_id;
END $$;

COMMIT;

-- Verify the super admin was created
SELECT 
    u.id,
    u.email,
    u.first_name || ' ' || u.last_name as full_name,
    u.role,
    u.is_active,
    u.email_verified,
    t.name as tenant_name,
    t.subscription_tier
FROM users u
LEFT JOIN tenant_users tu ON tu.user_id = u.id
LEFT JOIN tenants t ON t.id = tu.tenant_id
WHERE u.email = 'admin@weedgo.ca';